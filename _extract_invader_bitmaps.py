from PIL import Image

alien_bitmaps = []

for i in range(1, 4):
    im = Image.open('alien' + str(i) + '.png')
    white_pixels = []
    for x in range(im.width):
        for y in range(im.height):
            if im.getpixel((x, y)) == 0:
                white_pixels.append((x, y))
    alien_bitmaps.append(white_pixels)

print(alien_bitmaps)
